public class EvenOdd 
{

	public static void main(String[] args) 
	{
		int x = 15;
		int temp = x/2;
		
		if(temp*2==x)
		{
			System.out.println("It is an Even Number");
		}
		else
		{
			System.out.println("It is an Odd number");
		}

	}

}
